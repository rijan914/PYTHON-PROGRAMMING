#TYPECASTING
#Typecasting is a way to change the data type of a value and it is used in Python to convert one data type to another.
a="1"
b="3"
print(a+b)
c="type"
d="casting"
print(c+d)
#explicit typecasting
a=int(a)
b=int(b)
print(a+b)
c=str(c)
d=str(d)
print(c+d)

#implicit typecasting
a=1
b=3
print(a+b)